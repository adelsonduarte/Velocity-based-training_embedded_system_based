void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef* htim)
{
	/*50 Hz -> 20
	100 hz -> 10
	200 hz ->5
	500 Hz -> 2
	1k -> 1*/
	if(timerFlag == TRUE )
	{
		newTime+=1; //1ms
		if(newTime == ptr_dataDevice->timeSampling)
		{
			readFlag = 0;
			bufferPulso[samples] = pulseCounter;
			acquiredTime += newTime;
			currentTime[samples] = acquiredTime;
			samples++;
			newTime = 0;
			readFlag = TRUE;
		}
		if(samples == SAMPLES)
		{
			transmitFlag = TRUE;
			samples=0;
		}
	}

}

